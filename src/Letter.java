public class Letter {
    private char let;
    private int count;

    public Letter(char let, int count) {
        this.let = let;
        this.count = count;
    }

    public char getLet() {
        return let;
    }

    public void setLet(char let) {
        this.let = let;
    }

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }
}
